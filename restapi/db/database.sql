CREATE TABLE location_table (
    location_id INT GENERATED ALWAYS AS IDENTITY ,
    city_name VARCHAR(200),
    city_size INT ,
    countryName VARCHAR(100),
    continent VARCHAR (100)
);

ALTER TABLE location_table ADD CONSTRAINT location_idPK PRIMARY KEY (location_id);

CREATE TABLE user_table (
    user_id INT GENERATED ALWAYS AS IDENTITY ,
    nick_name VARCHAR(30),
    email VARCHAR(60),
    password VARCHAR(40),
    gender VARCHAR(10) ,
    age INT ,
    location_id INT NOT NULL
);

ALTER TABLE user_table ADD CONSTRAINT user_tablePK PRIMARY KEY (user_id);
ALTER TABLE user_table ADD CONSTRAINT location_idFK FOREIGN KEY (location_id) REFERENCES location_table(location_id);

CREATE TABLE user_role (
   user_role_id INT GENERATED ALWAYS AS IDENTITY ,
   user_role_name VARCHAR(40) ,
   user_role_desc VARCHAR(255)
);

ALTER TABLE user_role ADD CONSTRAINT user_rolePK PRIMARY KEY (user_role_id);

CREATE TABLE user_table_role (
    user_id int NOT NULL ,
    user_role_id int NOT NULL
);

ALTER TABLE user_table_role ADD CONSTRAINT user_tableFK FOREIGN KEY (user_id) REFERENCES user_table (user_id);
ALTER TABLE user_table_role ADD CONSTRAINT user_roleFK FOREIGN KEY (user_role_id) REFERENCES user_role (user_role_id);


CREATE TABLE tracks (
    track_id VARCHAR(64) PRIMARY KEY
);

CREATE TABLE rating (
    rating_id INT GENERATED ALWAYS AS IDENTITY ,
    points INT ,
    user_id INT NOT NULL ,
    track_id VARCHAR(64) NOT NULL
);

ALTER TABLE rating ADD CONSTRAINT ratingPK PRIMARY KEY (rating_id);
ALTER TABLE rating ADD CONSTRAINT rating_userFK FOREIGN KEY (user_id) REFERENCES user_table (user_id);
ALTER TABLE rating ADD CONSTRAINT rating_trackFK FOREIGN KEY (track_id) REFERENCES tracks (track_id);


